def calculate_ma(klines):
    """ËÆ°ÁÆóMA9ÂíåMA26ÊåáÊ†á"""
    if not klines or len(klines) < 26:
        return 0, 0, 0
        
    closes = [float(k[4]) for k in klines]
    ma9 = sum(closes[-9:]) / 9
    ma26 = sum(closes[-26:]) / 26
    return ma9, ma26, closes[-1]

